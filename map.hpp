#include <SFML/Graphics.hpp>
#include <fstream>
#include <string>

int HEIGHT_MAP = 35;//размер карты высота
int WIDTH_MAP = 266;//размер карты ширина 

std::vector<sf::String> TileMap;

void readMap()
{
    std::ifstream file;
    file.open("map.txt");

    file >> HEIGHT_MAP >> WIDTH_MAP;
    std::string temp;

    TileMap = std::vector<sf::String> (HEIGHT_MAP);

    for (int i = 0; i < HEIGHT_MAP; ++i)
    {
        file >> temp;
        TileMap[i] = sf::String(temp);
    }
        
}
 
